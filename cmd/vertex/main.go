func newHash(s string) uint64 {
	hash := fnv.New64a()
	_, _ = hash.Write([]byte(s))
	return hash.Sum64()
}

func main() {
	fmt.Println(newHash("8yn0iYCKYHlIj4-BwPqk"), newHash("GReLUrM4wMqfg9yzV3KQ"))
}
